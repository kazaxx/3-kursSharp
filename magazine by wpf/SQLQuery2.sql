create database unclepiston
use unclepiston


CREATE TABLE Пользователь (
    ID_пользователь INT PRIMARY KEY IDENTITY,
    Логин_пользователя VARCHAR(255),
    Пароль_пользователя VARCHAR(255),
    Админ BIT DEFAULT 0,
    Сотрудник BIT DEFAULT 0,
    Пользователь BIT DEFAULT 0
);

CREATE TABLE Поставщики (
  ID_поставщика INT PRIMARY KEY,
  Название_поставщика VARCHAR(255),
  Адрес VARCHAR(255),
  Телефон VARCHAR(20)
);

CREATE TABLE Запчасти (
  ID_запчасти INT PRIMARY KEY,
  Название_запчасти VARCHAR(255),
  Производитель VARCHAR(255),
  Цена DECIMAL(10, 2),
  ID_поставщика INT,
  FOREIGN KEY (ID_поставщика) REFERENCES Поставщики(ID_поставщика)
);

CREATE TABLE Клиенты (
  ID_клиента INT PRIMARY KEY,
  Фамилия VARCHAR(255),
  Имя VARCHAR(255),
  Отчество VARCHAR(255),
  Адрес VARCHAR(255),
  Телефон VARCHAR(20)
);

CREATE TABLE Заказы (
  ID_заказа INT PRIMARY KEY,
  Дата_заказа DATE,
  ID_клиента INT,
  ID_состава INT,
  ID_сотрудника INT,
  Статус_заказа varchar(255),
  FOREIGN KEY (ID_клиента) REFERENCES Клиенты(ID_клиента),
  FOREIGN KEY (ID_сотрудника) REFERENCES Сотрудники(ID_сотрудника),
  FOREIGN KEY (ID_состава) REFERENCES Состав_заказа(ID_состава),
);

CREATE TABLE Статус_заказа (
  ID_Статус_заказа INT PRIMARY KEY,
  ID_заказа INT,
  Статус_заказа varchar(255),
  Доставщик_заказа varchar(255),
  Город_отправления_заказа varchar(255),
  Дата_получения varchar(255),
  Название VARCHAR(255),
  FOREIGN KEY (ID_заказа) REFERENCES Заказы( ID_заказа)
);

CREATE TABLE Склад (
  ID_склада INT PRIMARY KEY,
  ID_запчасти INT,
  Количество INT,
  FOREIGN KEY (ID_запчасти) REFERENCES Запчасти(ID_запчасти)
);


CREATE TABLE Продажи (
  ID_продажи INT PRIMARY KEY,
  ID_заказа INT,
  Дата_продажи DATE,
  Сумма DECIMAL(10, 2),
  FOREIGN KEY (ID_заказа) REFERENCES Заказы(ID_заказа)
);


CREATE TABLE Состав_заказа (
  ID_состава INT PRIMARY KEY,
  ID_запчасти INT,
  Количество INT,
  FOREIGN KEY (ID_запчасти) REFERENCES Запчасти(ID_запчасти)
);

CREATE TABLE Сотрудники (
  ID_сотрудника INT PRIMARY KEY,
  ID_users INT,
  ID_пользователь INT,
  Фамилия VARCHAR(255),
  Имя VARCHAR(255),
  Отчество VARCHAR(255),
  Дата_найма VARCHAR(255),
  Должность VARCHAR(255),
  Зарплата DECIMAL(10, 2),
  FOREIGN KEY (ID_пользователь) REFERENCES Пользователь(ID_пользователь)
);




